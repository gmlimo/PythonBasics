#A for loop that iterates 5 times (from 0 to 4)
print('first loop')
for i in range(5):
    print(i)

#For loop that iterates from 15 to 20 (displays 15 to 19)
print('second loop')
for i in range(15, 20):
    print(i)

#For loop that iterates from 1 to 20 in increments of 4
print('third loop')
for i in range(1, 20, 4):
    print(i)
