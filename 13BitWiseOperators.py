a = 6
b = 3

print( a & b ) #Prints the result of the operation a and b
print( a | b ) #Prints the result of the operation a or b
print( a ^ b ) #Prints the result of the operation a xor b
print( ~a ) #Prints the result of the operation of the complement of a
print( a << 2 ) #a shifted to the left 2 times
print( a >> 2 ) #a shifted to the right 2 times
